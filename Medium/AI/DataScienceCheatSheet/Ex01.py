# 1. General Python Basics
# Data Types
x = 10            # int
y = 10.5          # float
z = "Hello"       # str

# Lists
my_list = [1, 2, 3, 4]
my_list.append(5)

# Dictionaries
my_dict = {"name": "Alice", "age": 25}
my_dict["city"] = "New York"

# Loops
for i in range(5):
    print(i)

# Functions
def square(x):
    return x ** 2

# Lambda Functions
add = lambda a, b: a + b
print(add(1, 4))